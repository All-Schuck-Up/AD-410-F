const mongoose = require('mongoose');

const ProviderSchema = new mongoose.Schema({ //providers schema
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});

module.exports = Provider = mongoose.model('provider', ProviderSchema);
